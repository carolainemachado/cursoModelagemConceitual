package com.carolaine.cursomod_conceitual.services;

import java.util.Optional;

import org.hibernate.ObjectNotFoundException;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import com.carolaine.cursomod_conceitual.domain.Pedido;
import com.carolaine.cursomod_conceitual.repositories.PedidoRepository;

@Service
public class PedidoService {

	@Autowired
	private PedidoRepository repo;
	
	public Pedido find(Integer id) {
		Optional<Pedido> obj = repo.findById(id);
		return obj.orElseThrow(() -> new ObjectNotFoundException(
		"Objeto n√£o encontrado! Id: " + id + ", Tipo: " + Pedido.class.getName(), obj));
		}
}


